var keys = Object.keys(definitions);

keys.forEach(function(hira) {
  var conv = ime.convert(hira);
  definitions[hira].katakana = conv.katakana;
});

var searchMeaning = function(key, term) {
  var entry = definitions[key];
  var cats = Object.keys(entry), cat, meanings, meaning, i, last, j, llast;
  for(i=0, last=cats.length; i<last; i++) {
    cat = cats[i];
    meanings = entry[cat];
    for(j=0, llast=meanings.length; j<llast; j++) {
      meaning = meanings[j];
      if(meaning.indexOf(term) > -1) {
        return true;
      }
    }
  }
  return false;
}

module.exports = {
  get: function(key) {
    var res = JSON.parse(JSON.stringify(definitions[key]));
    delete res.katakana;
    return res;
  },
  find: function(input) {
    var conv = ime.convert(input);
    // japanese search
    if(conv.katakana) {
      var res = {},
          get = this.get,
          kana = conv.katakana;
      keys.forEach(function(key) {
        if(definitions[key].katakana.indexOf(kana) === 0) {
          res[key] = get(key);
        }
      });
      return res;
    }
    // english search
    if(conv === false) {
      var res = {},
          get = this.get;
      keys.forEach(function(key) {
        if(searchMeaning(key, input)) {
          res[key] = get(key);
        }
      });
      return res;
    }
  }
};
